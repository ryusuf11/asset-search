<script setup lang="ts">
const footerRef = useTemplateRef<HTMLElement | null>('footerRef');
const isHeaderSticky = ref(true);

let observer: IntersectionObserver;

onMounted(() => {
  observer = new IntersectionObserver(
    (entries) => {
      for (const entry of entries) {
        if (entry.target === footerRef.value) {
          isHeaderSticky.value = !entry.isIntersecting;
        }
      }
    },
    {
      threshold: 0.8,
      rootMargin: '0px 0px -40px 0px',
    }
  );

  if (footerRef.value) {
    observer.observe(footerRef.value);
  }
});

onBeforeUnmount(() => {
  if (observer && footerRef.value) {
    observer.unobserve(footerRef.value);
  }
});

watch(isHeaderSticky, (value) => {
  setTimeout(() => {
    document.querySelector('.headerNav')?.classList.toggle('headerNav--sticky', value);
  }, 200);
});
</script>

